<h1>Lista de días especiales</h1>

<p>Permite definir días especiales, festivos y días laborables especiales, en las plantillas de alertas. Estos días serán tratados del mismo modo que el día de la semana seleccionado.</p>

<p>Por ejemplo, supongamos que el 3 de Mayo de 2012 es festivo. Cuando definimos el '2012-05-03' como 'domingo', este día será tratado como si fuese un domingo. El formato de fecha es YYYY-MM-DD. Si no se quiere especificar el año, se puede sustituir YYYY por '*'.
</p>

<p>
Nota: Para habilitar la lista de días especiales, "Utilizar lista de días especiales" tiene que estar activado en la plantilla de la alerta (paso2).
</p>
