<h1>Module macros</h1>

Any number of custom module macros may be defined. The recommended format for macro names is:

<pre>
_macroname_
</pre>

For example:
<ul>
<li>_technology_</li>
<li>_modulepriority_</li>
<li>_contactperson_</li>
</ul>

This macros can be used in module alerts.
